/**
 * Adding an additional column for customer card data
 */

$installer = $this;
$installer->startSetup();
$setup = new Mage_Sales_Model_Resource_Setup('core_setup');

$installer->getConnection()->addColumn($this->getTable('sales/order'),
     'xp_card_data',
     "text NOT NULL"
);

$installer->endSetup();